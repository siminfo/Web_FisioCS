                        <p>
                           La Fisioterapia es la ciencia del tratamiento a través de: medios físicos, ejercicio terapéutico, masoterapia y electroterapia. El fisioterapeuta utiliza la aplicación de agentes físicos en personas afectadas de diversos problemas mecánicos con el fin de curar, prevenir, o recuperar las más diversas dolencias orgánicas y así mejorar su calidad de vida.
