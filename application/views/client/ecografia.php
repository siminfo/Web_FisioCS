                        <h2>Ecografía</h2>

                        <p>
                       La ecografía es una técnica de diagnóstico que emplea el ultrasonido para crear imágenes bidimensionales o tridimensionales. Al someterse a un examen de ecografía, el paciente sencillamente se acuesta sobre una camilla y el fisioterapeuta mueve la sonda o transductor sobre la piel que se encuentra sobre la parte del cuerpo a examinar. Antes es preciso colocar un gel sobre la piel para la correcta transmisión de los ultrasonidos.

Es una técnica de imagen muy sencilla que permite observar y detectar problemas en músculos, articulaciones, ligamentos, tendones, etc., y así valorar objetivamente el tejido lesionado.

Con ello, se permite ver la evolución de todo tipo de lesiones musculoesqueléticas y aplicar técnicas de tratamiento apropiadas a la cronología de las mismas.

Este tipo de prueba, nos permite valorar cómo evoluciona la lesión con el tratamiento aplicado en sucesivas sesiones y está cada vez más respaldada científicamente, lo que avala su uso en el proceso de razonamiento clínico. Es una prueba inocua para los pacientes y supone un complemento a la historia clínica, la exploración física u otro tipo de pruebas, lo que resulta de gran utilidad para el fisioterapeuta.
                        </p>
                     

