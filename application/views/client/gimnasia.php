                        <h2>Gimnasia Abdominal Hipopresiva</h2>
                        <p>
                      La Gimnasia Abdominal Hipopresiva®️ (GAH) es una disciplina que aporta un mejor funcionamiento del cuerpo sin aumentar la presión intraabdominal. 

<br>
El objetivo de esta gimnasia es regular y compensar los efectos de la hiperpresión abdominal (mala postura, profesiones sedentarias o de alta carga física, actividad deportiva) mediante la activación de los músculos posturales (músculos profundos de la columna, transverso abdominal y suelo pélvico) y la normalización de los músculos potencialmente hiperpresivos (diafragma y recto abdominal, conocido como “la tableta de chocolate”).  

                        </p>
                        
                         

                    </div>


                <h4>Es, por tanto, una gimnasia indicada para los siguientes casos:</h4>
                      
                        <div class="row">
                            

                        <div class="col-md-12 info" >
                            
                            <ul>
                                
                                <li>Madres en fase de post-parto</li>
                                <li>Disfunciones de suelo pélvico: incontinencia urinaria, prolapsos</li>
                                <li>Alteración de la postura y dolores derivados</li>
                                <li>Patología de columna: hernias discales</li>
                                <li>Trastornos digestivos: estreñimiento, gases, colon irritable</li>
                                <li>Problemas respiratorios</li>
                                 <li>Mejora del rendimiento deportivo</li>
                                 <li>Estados de ansiedad y depresión</li>
                              
                            </ul>


En nuestra clínica la gimnasia es programada de manera individualizada y desde una perspectiva terapéutica, realizando una entrevista y un examen físico previos a cada individuo. Todo el proceso terapéutico es llevado a cabo por una fisioterapeuta cualificada en la Gimnasia Abdominal Hipopresiva®️. 

<br>
Para garantizar la calidad del servicio, Clínica Serrano ofrece clases individuales o clases de grupos reducidos de hasta 4 personas, cuya duración es de 45 minutos
<br>
Si los casos mencionados arriba son familiares para Usted, no dude en ponerse en contacto con nosotros. 

